defmodule AdminManager.ShipCost do
  use AdminManager.Web, :model

  schema "ship_costs" do

    timestamps()
  end

  @doc """
  Builds a changeset based on the `struct` and `params`.
  """
  def changeset(struct, params \\ %{}) do
    struct
    |> cast(params, [])
    |> validate_required([])
  end
end
